CREATE DATABASE LOGINFORM;

USE LOGINFORM;

CREATE TABLE LOGINS (
ID INT IDENTITY(1,1),
USUARIO VARCHAR(50) NOT NULL,
CLAVE VARCHAR(50) NOT NULL
CONSTRAINT PK_LOGINS PRIMARY KEY (ID)
);

