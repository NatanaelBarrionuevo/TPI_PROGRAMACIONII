CREATE PROCEDURE SP_LOGIN
@USUARIO_INGRESADO VARCHAR(50),
@CLAVE_INGRESADA VARCHAR(50)
AS
  BEGIN
  SELECT TOP 1 COUNT(*)
  FROM LOGINS 
  WHERE LOGINS.USUARIO = @USUARIO_INGRESADO
  AND LOGINS.CLAVE = @CLAVE_INGRESADA
  END;

  --PRUEBA USER & CLAVE BIEN
  EXECUTE SP_LOGIN 'VALENTINA','1234'
  -- PRUEBA USER & CLAVE MAL
  EXECUTE SP_LOGIN 'VALENTINA','3333'

 